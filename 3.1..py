import math

# Pierwiastek kwadratowy z liczby 7
sqrt_value = math.sqrt(7)
print('Pierwiastek kwadratowy z 7:', sqrt_value)

# Logarytm naturalny z 5
log_value = math.log(5)
print('Logarytm naturalny z 5:', log_value)

# Sinus kąta 90° (w radianach to pi/2)
sin_value = math.sin(math.pi / 2)
print('Sinus kąta 90°:', sin_value)

