# Największa liczba z 7, 5, 6, 3, 8, 2
max_number = max(7, 5, 6, 3, 8, 2)
print('Max number:', max_number)

# Najmniejsza liczba z 4, 7, 2, 3, 9, 8
min_number = min(4, 7, 2, 3, 9, 8)
print('Min number:', min_number)

# Długość frazy "computer science"
str_length = len("computer science")
print('Długość frazy "computer science":', str_length)

# Wczytanie litery z klawiatury
letter = input('Wpisz literę: ')
print('Wpisana litera to:', letter)

# Liczba reprezentująca ciąg "20303"
number = int("20303")
print('Liczba z ciągu "20303":', number)

# Binarny zapis liczby 304
binary_rep = bin(304)
print('Binarny zapis liczby 304:', binary_rep)

# Szesnastkowy zapis liczby 304
hex_rep = hex(304)
print('Szesnastkowy zapis liczby 304:', hex_rep)


# Unicode dla znaku "€"
unicode_code = ord('€')
print('Kod Unicode dla znaku "€":', unicode_code)

# Wartość bezwzględna liczby -17
abs_value = abs(-17)
print('Wartość bezwzględna -17:', abs_value)
